package CricketApplication;

public class EveryBallScore {
	private String strikerName;
	private String runsInBall;
	public String getStrikerName() {
		return strikerName;
	}
	public void setStrikerName(String strikerName) {
		this.strikerName = strikerName;
	}
	public String getRunsInBall() {
		return runsInBall;
	}
	public void setRunsInBall(String runsInBall) {
		this.runsInBall = runsInBall;
	}
	

}
